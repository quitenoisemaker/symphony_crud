    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $getItem = Item::findOrFail($id);

        Storage::disk('public')->delete($getItem->image);
        $getItem->delete();

        return back();
    }
